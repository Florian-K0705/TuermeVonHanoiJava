	public static String encode(String keyword, String klartext)
	{
		StringBuilder bl = new StringBuilder();
		keyword = keyword.toUpperCase();
		klartext = klartext.toUpperCase();
		
		for (int i=0; i<klartext.length(); i++)
		{
			if (!contains(String.valueOf(klartext.charAt(i)), polyaplhabet[0]))
			{
				bl.append("_");
				continue;
			}
			
			int current1 = Arrays.binarySearch(polyaplhabet[0], String.valueOf(klartext.charAt(i)));
			int current2 = Arrays.binarySearch(polyaplhabet[0], String.valueOf(keyword.charAt(i % keyword.length())));
			
			bl.append(polyaplhabet[current2][current1]);
			
		}
		
		return bl.toString();
	}
	
	
	public static boolean contains(String a, String[] arr)
	{
		boolean result = false;
		
		for (int i=0; i<arr.length; i++)
		{
			if (a.equals(arr[i]))
			{
				result = true;
				break;
			}
		}
		
		return result;
	}
